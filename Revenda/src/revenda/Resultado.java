    private void insereTabela(ResultSet rs) throws SQLException {
        Object obj[] = new Object[6];

        while (rs.next()) {
            obj[0] = rs.getString("modelo");
            obj[1] = rs.getString("cor");
            obj[2] = rs.getDouble("investimento");
            obj[3] = rs.getString("estado_conservacao");
            obj[4] = rs.getDouble("similaridade");

            DefaultTableModel model = (DefaultTableModel) tabelaResultado.getModel();
            model.addRow(obj);
        }
    }

